package cn.emagsoftware.xfb.pojo;

/**
 * 应用信息
 *
 * @author LongHuan
 */
public class App {

    private Long appID;

    private String appVersion;
    ;

    private String appSize;

    private String appUrl;

    /**
     * 应用类型 1-android 2-ios
     */
    private String appType;

    /**
     * 应用渠道编码
     */
    private String appChannelCode;

    private String appDescription;

    public String getAppDescription() {
        return appDescription;
    }

    public void setAppDescription(String appDescription) {
        this.appDescription = appDescription;
    }

    public Long getAppID() {
        return appID;
    }

    public void setAppID(Long appID) {
        this.appID = appID;
    }

    public String getAppVersion() {
        return appVersion;
    }

    public void setAppVersion(String appVersion) {
        this.appVersion = appVersion;
    }

    public String getAppSize() {
        return appSize;
    }

    public void setAppSize(String appSize) {
        this.appSize = appSize;
    }

    public String getAppUrl() {
        return appUrl;
    }

    public void setAppUrl(String appUrl) {
        this.appUrl = appUrl;
    }

    public String getAppType() {
        return appType;
    }

    public void setAppType(String appType) {
        this.appType = appType;
    }

    public String getAppChannelCode() {
        return appChannelCode;
    }

    public void setAppChannelCode(String appChannelCode) {
        this.appChannelCode = appChannelCode;
    }

}
