package cn.emagsoftware.frame.service;

import cn.emagsoftware.frame.log4j.ILog;
import cn.emagsoftware.frame.log4j.Logger;


/**
 * @Title: 业务类接口基类
 */
public class BaseService {

	@Logger
	protected static ILog log;
    /**
     * NULL字符串转换成""
     * 
     * @param str
     * @return
     */
    public String nvl(String str) {
        if (str == null)
            return "";
        else
            return str;
    }
}